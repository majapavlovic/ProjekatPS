    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selected=tblProducts.getSelectedRow();
        if(selected!=-1){
            TableModelProduct tm=(TableModelProduct)tblProducts.getModel();
            tm.removeProduct(selected);
        }else{
            JOptionPane.showMessageDialog(this, "No row selected!", "Selection", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        TableModelProduct tm=(TableModelProduct)tblProducts.getModel();
        List<Product> products=tm.getProducts();
        for (Product product : products) {
            product.setSavedByUser(Controller.getInstance().getCurrentUser());
        }
        try {
            Controller.getInstance().addAllProducts(products);
            JOptionPane.showMessageDialog(this, "Success!", "Save", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska "+ex.getMessage(), "Save", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

