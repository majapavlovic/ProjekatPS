    private void prepareTableProduct() throws SQLException {
        List<Product> products = new ArrayList<>();
        TableModelProduct tm = new TableModelProduct(products);
        tblProducts.setModel(tm);

        List<Manufacturer> manufacturers = Controller.getInstance().getAllManufacturers();
        JComboBox cbManufacturers = new JComboBox(manufacturers.toArray());

        TableColumn tcManufacturer = tblProducts.getColumnModel().getColumn(5);
        tcManufacturer.setCellEditor(new DefaultCellEditor(cbManufacturers));

        JComboBox cbMeasurementUnits = new JComboBox(MeasurementUnit.values());
        TableColumn tcMeasurementUnit = tblProducts.getColumnModel().getColumn(4);
        tcMeasurementUnit.setCellEditor(new DefaultCellEditor(cbMeasurementUnits));

    }

