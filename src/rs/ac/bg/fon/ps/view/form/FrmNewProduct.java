    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            // TODO Validation
            Product product = new Product();
            product.setId(Long.parseLong(txtID.getText().trim()));
            product.setName(txtName.getText().trim());
            product.setDescription(txtDescription.getText().trim());
            product.setPrice(new BigDecimal(txtPrice.getText().trim()));
            product.setMeasurementUnit((MeasurementUnit) cmbMeasurementUnit.getSelectedItem());
            product.setManufacturer((Manufacturer) cmbManufacturer.getSelectedItem());
            product.setSavedByUser(Controller.getInstance().getCurrentUser());
            Controller.getInstance().addProduct(product);
            JOptionPane.showMessageDialog(this, "Product is saved!");
            System.out.println(Controller.getInstance().getAllProducts());
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: \n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


