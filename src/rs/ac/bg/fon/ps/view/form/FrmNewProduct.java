    private void prepareView() throws Exception {
        fillCmbMeasurementUnit();
        fillCmbManufacturer();
    }

    private void fillCmbMeasurementUnit() throws Exception {
        cmbMeasurementUnit.removeAllItems();
        for (MeasurementUnit unit : MeasurementUnit.values()) {
            cmbMeasurementUnit.addItem(unit);
        }
    }

    private void fillCmbManufacturer() throws Exception {
        cmbManufacturer.removeAllItems();
        List<Manufacturer> manufacturers = Controller.getInstance().getAllManufacturers();
        for (Manufacturer manufacturer : manufacturers) {
            cmbManufacturer.addItem(manufacturer);
        }
    }
